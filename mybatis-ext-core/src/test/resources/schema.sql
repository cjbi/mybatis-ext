drop table T_GOODS;
create table T_GOODS
(
	ID INT NOT NULL GENERATED BY DEFAULT AS IDENTITY (START WITH 10000) PRIMARY KEY,
	CATEGORY_ID INTEGER default 0,
	GOODS_SN VARCHAR(255),
	NAME VARCHAR(255),
	BRAND_ID INTEGER default 0,
	GOODS_UNIT VARCHAR(255),
	GOODS_BRIEF VARCHAR(255),
	GOODS_DESC CLOB,
	IS_ON_SALE INTEGER default 1,
	CREATE_TIME TIMESTAMP default CURRENT_TIMESTAMP,
	SORT_ORDER INTEGER default 100,
	IS_DELETE INTEGER default 0,
	ATTRIBUTE_CATEGORY INTEGER default 0,
	COUNTER_PRICE DECIMAL(10,2) default 0.00,
	EXTRA_PRICE DECIMAL(10,2) default 0.00,
	IS_NEW INTEGER default 0,
	KEYWORDS VARCHAR(255) default 'NULL',
	GOODS_NUMBER INTEGER default 0,
	LIST_PIC_URL VARCHAR(255),
	RETAIL_PRICE DECIMAL(10,2) default 0.00,
	SELL_VOLUME INTEGER default 0,
	PRIMARY_PRODUCT_ID INTEGER default 0,
	UNIT_PRICE DECIMAL(10,2) default 0.00,
	PROMOTION_DESC VARCHAR(255),
	PROMOTION_TAG VARCHAR(255),
	APP_EXCLUSIVE_PRICE DECIMAL(10,2),
	IS_APP_EXCLUSIVE INTEGER,
	IS_LIMITED INTEGER,
	IS_HOT INTEGER default 0,
	PRIMARY_PIC_URL VARCHAR(255),
	VERSION INTEGER default 0
);

create index BRAND_ID
	on T_GOODS (BRAND_ID);

create index CAT_ID
	on T_GOODS (CATEGORY_ID);

create index GOODS_NUMBER
	on T_GOODS (GOODS_NUMBER);

create index GOODS_SN
	on T_GOODS (GOODS_SN);

create index SORT_ORDER
	on T_GOODS (SORT_ORDER);

